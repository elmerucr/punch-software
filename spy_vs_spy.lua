local color = 10

local ticks
local counter

function init()
  -- make blitter cause interrupts at start of frame
  -- this will call the lua function frame()
  pokeb(0x801, 1)

  -- timer stuff
  pokew(0xa10, 3008)
  pokeb(0xa01, 1)

  -- init sound
  ticks = 50
  counter = 1
  -- pulse width
  pokew(0xc02, 0x0f0f)
  pokew(0xc0a, 0x0f0f)
end

function timer0()
  do_sound()
end

function frame()
  pokeb(0xe05, color)	-- target color
  pokeb(0xe03, 0xf)	-- target surface = 0xf
  pokeb(0xe01, 4)	-- clear surface command
  color = color + 1
  if color == 20 then color = 10 end
end

