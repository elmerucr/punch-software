function do_sound()
    if ticks == 0 then
        if track1[counter] ~= 0 then
            pokew(0xc80, track1[counter])
            pokeb(0xc85, 0x04)
	    pokeb(0xc86, 0x15)
	    pokeb(0xc84, 0x41)
        end
        if track2[counter] ~= 0 then
	    pokew(0xc88, track2[counter])
	    pokeb(0xc8d, 0x04)
	    pokeb(0xc8e, 0x15)
            pokeb(0xc8c, 0x41)
        end
        ticks = 10
        counter = counter + 1
        if counter == (#track1 + 1) then
            counter = 1
        end
    elseif ticks == 2 then
        pokeb(0xc85, 0x00)
	pokeb(0xc86, 0x00)
	pokeb(0xc84, 0x40)

	pokeb(0xc8d, 0x00)
	pokeb(0xc8e, 0x00)
	pokeb(0xc8c, 0x40)
    end
    ticks = ticks - 1
end
